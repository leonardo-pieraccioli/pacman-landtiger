void draw_square_cell(uint16_t xpos, uint16_t ypos, uint16_t color, uint16_t dimension)
{
	int i,j;
	for(i = 0; i < dimension; i++)
	{
		LCD_DrawLine(xpos, ypos + i, xpos + dimension - 1, ypos + i, color);
	}
}
	
